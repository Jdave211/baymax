#!/usr/bin/env node
/**
 * Writes dist/site-config.js (and optionally landing-page/site-config.js) from env vars.
 * Do not commit real keys — set SUPABASE_URL, SUPABASE_ANON_KEY, STRIPE_PUBLISHABLE_KEY in CI or Netlify.
 *
 * Usage: node scripts/generate-site-config.mjs [outputPath]
 * Default output: dist/site-config.js
 */
import fs from "fs";
import path from "path";

const outPath = path.resolve(process.argv[2] || "dist/site-config.js");
const config = {
  supabaseUrl: process.env.SUPABASE_URL || "",
  supabaseAnonKey: process.env.SUPABASE_ANON_KEY || "",
  stripePublishableKey: process.env.STRIPE_PUBLISHABLE_KEY || "",
  workerUrl: process.env.WORKER_URL || "https://clicky-proxy.baymac.workers.dev",
};

fs.mkdirSync(path.dirname(outPath), { recursive: true });
const body = `// Generated by scripts/generate-site-config.mjs — do not edit by hand with real secrets in git
window.BAYMAC_SITE = ${JSON.stringify(config, null, 2)};
`;
fs.writeFileSync(outPath, body, "utf8");
console.log("Wrote", outPath);
